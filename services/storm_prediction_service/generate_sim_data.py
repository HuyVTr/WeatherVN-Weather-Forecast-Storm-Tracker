import pandas as pd
import numpy as np
from pathlib import Path

def generate_storm_track(hours=168, initial_lat=8.0, initial_lon=130.0, deviation_factor=0.0):
    """Generates a DataFrame for a storm track."""
    lats = [initial_lat]
    lons = [initial_lon]
    
    # Simulate a west-northwest track
    # Add some non-linear movement
    lon_change = np.linspace(-0.2, -0.25, hours-1) + np.sin(np.linspace(0, 2*np.pi, hours-1)) * 0.02
    lat_change = np.linspace(0.1, 0.08, hours-1) + np.cos(np.linspace(0, 2*np.pi, hours-1)) * 0.03
    
    for i in range(hours - 1):
        # Add a small random deviation for the predicted path
        dev_lat = np.random.randn() * 0.05 * deviation_factor
        dev_lon = np.random.randn() * 0.05 * deviation_factor
        
        lats.append(lats[-1] + lat_change[i] + dev_lat)
        lons.append(lons[-1] + lon_change[i] + dev_lon)

    # Generate meteorological data
    # Wind speeds increase then decrease
    wind_profile = 15 + 45 * np.sin(np.linspace(0, np.pi, hours))
    # Pressure is inverse of wind
    pressure_profile = 1005 - 60 * np.sin(np.linspace(0, np.pi, hours))
    
    # Other features vary smoothly
    sst = np.linspace(29.5, 27.5, hours)
    humidity = np.linspace(92, 85, hours)
    temp = np.linspace(28, 26, hours)
    wind_shear = np.linspace(5, 15, hours)
    
    # Add some noise for the predicted path
    if deviation_factor > 0:
        wind_profile += np.random.randn(hours) * 2
        pressure_profile += np.random.randn(hours) * 3
        
    u10 = wind_profile * np.cos(np.radians(290)) # Assuming a general 290-degree movement for component calculation
    v10 = wind_profile * np.sin(np.radians(290))

    df = pd.DataFrame({
        'LATITUDE': lats,
        'LONGITUDE': lons,
        'U10': u10,
        'V10': v10,
        'PRMSL': pressure_profile,
        'T_CELSIUS': temp,
        'SST': sst,
        'HUMIDITY': humidity,
        'WIND_SHEAR': wind_shear
    })
    
    return df

if __name__ == "__main__":
    output_dir = Path('simulation_data')
    output_dir.mkdir(exist_ok=True)

    # Generate actual path (no deviation)
    df_actual = generate_storm_track(hours=168, deviation_factor=0.0)
    actual_path_file = output_dir / 'actual_path.csv'
    df_actual.to_csv(actual_path_file, index=False)
    print(f"✅ Generated and saved actual storm path to '{actual_path_file}'")

    # Generate predicted path (with deviation)
    df_predicted = generate_storm_track(hours=168, deviation_factor=1.0)
