    # and then append the model's "prediction"
    predicted_input_part = df_actual.head(72)
    predicted_output_part = df_predicted.iloc[72:]
    
    # This is a simplified prediction; the real one will be generated by the model
    # For now, we create a placeholder predicted_path.csv that is different from actual_path
    # but has the same length. The `final_storm_forecast.py` will use the first 72h of actual_path
    # to generate the *real* prediction. This file is more of a backup/reference.
    df_predicted_full = pd.concat([predicted_input_part, predicted_output_part]).reset_index(drop=True)
    predicted_path_file = output_dir / 'predicted_path.csv'
    df_predicted.to_csv(predicted_path_file, index=False) # Saving the fully deviated one for now
